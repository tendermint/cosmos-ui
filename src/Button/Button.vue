<script>
export default {
  props: {
    size: {
      /**
       * `xs` | `s` | `m` | `l` | `xl`
       */
      type: String,
      default: "m"
    },
    /**
     * CSS color of `regular` | `danger`
     */
    background: {
      type: String,
      default: "rgb(80, 100, 251)"
    },
    /**
     * Disabled
     */
    disabled: {
      type: Boolean,
      default: false
    },
    /**
     * Type
     */
    type: {
      type: String,
      default: "submit"
    },
    /**
     * Tag
     */
    tag: {
      type: String,
      default: "button"
    },
    /**
     * href
     */
    href: {
      type: String,
      default: null
    },
    /**
     * target
     */
    target: {
      type: String,
      default: null
    }
  },
  computed: {
    buttonStyle() {
      const
        styles = ['regular', 'danger'],
        exists = styles.indexOf(this.background)
      if (exists >= 0) {
        return styles[exists]
      } else {
        return "standard"
      }
    }
  }
}
</script>