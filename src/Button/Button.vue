<template>
  <component class="component__button" :is="tag" v-bind="{target, type, disabled, href}" :class="[`button__size__${size}`, `button__style__${buttonStyle}`]">
    <slot/>
  </component>
</template>

<style scoped>
.component__button {
  border: none;
  font-family: var(--ds-font-family, sans-serif);
  font-size: initial;
  margin: 0;
  padding: 0;
  padding: .85em 1.15em;
  border-radius: .35em;
  cursor: pointer;
  user-select: none;
  outline: none;
  transition: all .25s;
  text-align: center;
  text-transform: uppercase;
  font-weight: 500;
  line-height: 1.25;
  letter-spacing: 0.02em;
  text-decoration: none;
}
